import path from "node:path";
import { publicJobSettings, sanitizeFolderName } from "./utils.js";

export function getDatasetLayout(settings) {
  const rootName = sanitizeFolderName(settings.folderName || "lora-dataset");
  const tokenPhrase = [settings.triggerToken, settings.classToken].filter(Boolean).join(" ");
  const subjectName = sanitizeFolderName(
    tokenPhrase.replace(/\s+/g, "-") || settings.classToken || "captioned-images"
  );
  const repeats = Math.max(1, Math.round(Number(settings.repeats) || 10));
  const trainingFolder = `${repeats}_${subjectName}`;

  return {
    rootName,
    tokenPhrase,
    repeats,
    trainingFolder,
    trainParentPath: `${rootName}/train`,
    trainingPath: `${rootName}/train/${trainingFolder}`,
    captionExtension: ".txt"
  };
}

export function captionedItems(job) {
  return job.items.filter((item) => item.status === "done" && item.caption);
}

