export function buildKohyaDatasetToml(job, layout) {
  return [
    "# Dataset config for kohya_ss / sd-scripts style LoRA training.",
    "# Paths are relative to the extracted ZIP root.",
    "",
    "[general]",
    "shuffle_caption = true",
    'caption_extension = ".txt"',
    "keep_tokens = 1",
    "enable_bucket = true",
    "",
    "[[datasets]]",
    "resolution = 1024",
    "batch_size = 1",
    "",
    "  [[datasets.subsets]]",
    `  image_dir = "./train/${layout.trainingFolder}"`,
    `  num_repeats = ${layout.repeats}`,
    layout.tokenPhrase ? `  class_tokens = "${escapeToml(layout.tokenPhrase)}"` : null,
    '  caption_extension = ".txt"',
    ""
  ]
    .filter((line) => line !== null)
    .join("\n");
}

export function buildCaptionJsonl(job, layout) {
  return captionedItems(job)
    .map((item) => {
      const ext = path.extname(item.storedName);
      const baseName = path.basename(item.storedName, ext);
      return JSON.stringify({
        file_name: `train/${layout.trainingFolder}/${item.storedName}`,
        caption_file: `train/${layout.trainingFolder}/${baseName}.txt`,
        caption: item.caption,
        original_name: item.originalName
      });
    })
    .join("\n")
    .concat("\n");
}

export function buildCaptionCsv(job, layout) {
  const rows = [["file_name", "caption_file", "caption", "original_name"]];

  for (const item of captionedItems(job)) {
    const ext = path.extname(item.storedName);
    const baseName = path.basename(item.storedName, ext);
    rows.push([
      `train/${layout.trainingFolder}/${item.storedName}`,
      `train/${layout.trainingFolder}/${baseName}.txt`,
      item.caption,
      item.originalName
    ]);
  }

  return `${rows.map((row) => row.map(escapeCsv).join(",")).join("\n")}\n`;
}

export function buildMetadata(job, layout) {
  return JSON.stringify(
    {
      id: job.id,
      createdAt: job.createdAt,
      finishedAt: job.finishedAt || new Date().toISOString(),
      updatedAt: job.updatedAt,
      total: job.total,
      processed: job.processed,
      failed: job.failed,
      layout,
      settings: publicJobSettings(job.settings),
      items: job.items.map((item) => ({
        originalName: item.originalName,
        storedName: item.storedName,
        status: item.status,
        caption: item.caption,
        error: item.error,
        editedAt: item.editedAt || ""
      }))
    },
    null,
    2
  );
}

function escapeCsv(value) {
  const text = String(value ?? "");
  return `"${text.replace(/"/g, '""')}"`;
}

function escapeToml(value) {
  return String(value ?? "").replace(/\\/g, "\\\\").replace(/"/g, '\\"');
}
